#include <bits/stdc++.h>
using namespace std;

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);

	int n, m;
	cin >> n >> m;
	int l[n], r[n];

	for (int i = 0; i < n; ++i) {
		cin >> l[i];
		cin >> r[i];
	}

	while (m--) {
		int q;
		int p[q];

		for (int i = 0; i < q; ++i)
			cin >> p[i];
	}

	return 0;
}

